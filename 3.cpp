#include <iostream> 
using namespace std;

int main() {
    // all data types available
    int x = 20;
    cout << x << endl;

    string y = "jonathan";
    cout << y << endl;

    float z = 7.89;
    cout << z << endl;

    bool a = true;
    cout << a << endl;

    char b = 'b';
    cout << b << endl;

    // can declare then use later
    int c, d;
    c = 20;
    d = 25;
    cout << c << d;
}

